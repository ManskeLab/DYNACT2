class Colours:
    WHITE = "\033[0m"  # For regular text
    RED = "\033[31m"  # For errors
    GREEN = "\033[32m"  # For success/completion
    ORANGE = "\033[33m"  # For warnings
    BLUE = "\033[34m"  # For secondary headers
    PURPLE = "\033[35m"  # For debugging
    BOLD = "\033[1m"  # For main headers
    UNDERLINE = "\033[4m"
